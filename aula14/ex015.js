/*
O for tem a mesma função que o while, funcionando da seguinte forma:
for(x;y;z){
}
x -> Inicia a variável
y -> Faz o teste lógico
z -> Faz o incremento
{} -> É o bloco que executa a função enquanto o teste lógico for verdadeiro (true)
*/
console.log("Vai começar...")
for(var c= 1;c <= 5; c++) {
console.log(c)
}
console.log("Fim!")
